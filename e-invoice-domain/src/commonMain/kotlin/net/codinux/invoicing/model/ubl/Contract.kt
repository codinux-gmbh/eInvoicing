  val nominationDate: Date? = null,
  @XmlSerialName(
    value = "NominationTime",
    prefix = "cbc",
    namespace = "urn:oasis:names:specification:ubl:schema:xsd:CommonBasicComponents-2",
  )
  @XmlElement(value = true)
  val nominationTime: Time? = null,
  @XmlSerialName(
    value = "ContractTypeCode",
    prefix = "cbc",
    namespace = "urn:oasis:names:specification:ubl:schema:xsd:CommonBasicComponents-2",
